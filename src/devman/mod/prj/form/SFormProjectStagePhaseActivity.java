    private void setFieldsStatusByProject() {
        if (moKeyProject.getSelectedIndex() <= 0) {
            moKeyDeliverable.setEnabled(false);
        }
        else {
            moKeyDeliverable.setEnabled(true);
        }
    }
    
    private void itemStateChangedProject() {
        if (moKeyProject.getSelectedIndex() <= 0) {
            moKeyDeliverable.removeAllItems();
        }
        else {
            miClient.getSession().populateCatalogue(moKeyDeliverable, SModConsts.PU_PRJ_DLV, SLibConsts.UNDEFINED, new SGuiParams(moKeyProject.getValue()));
        }
        
        setFieldsStatusByProject();
    }
    
