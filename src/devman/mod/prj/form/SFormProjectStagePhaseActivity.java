    @Override
    public void reloadCatalogues() {
        moKeyGroupStagePhase.initGroup();
        moKeyGroupStagePhase.addFieldKey(moKeyProject, SModConsts.PU_PRJ, SLibConsts.UNDEFINED, null);
        moKeyGroupStagePhase.addFieldKey(moKeyProjectStage, SModConsts.PU_PRJ_STG, SLibConsts.UNDEFINED, null);
        moKeyGroupStagePhase.addFieldKey(moKeyProjectStagePhase, SModConsts.PU_PRJ_STG_PHS, SLibConsts.UNDEFINED, null);
        moKeyGroupStagePhase.populateCatalogues();
    }

    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbProjectStagePhaseActivity) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            //moRegistry.setCode("");
            
            if (moRegistry.getPkProjectId() == SLibConsts.UNDEFINED) {
                moRegistry.setPkProjectId(((SGuiMainSessionCustom) miClient.getSession().getSessionCustom()).getProjectStageKey()[0]);
                moRegistry.setPkStageId(((SGuiMainSessionCustom) miClient.getSession().getSessionCustom()).getProjectStageKey()[1]);
                moRegistry.setDateStartPlanned(miClient.getSession().getCurrentDate());
                moRegistry.setDateEndPlanned(miClient.getSession().getCurrentDate());
            }
            
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moKeyProject.setValue(new int[] { moRegistry.getPkProjectId() });
        itemStateChangedProject();
        moKeyProjectStage.setValue(new int[] { moRegistry.getPkProjectId(), moRegistry.getPkStageId() });
        moKeyProjectStagePhase.setValue(new int[] { moRegistry.getPkProjectId(), moRegistry.getPkStageId(), moRegistry.getPkPhaseId()});
        moTextCode.setValue(moRegistry.getCode());
        moTextName.setValue(moRegistry.getName());
        jtaDescription.setText(moRegistry.getDescription());
        jtaDescription.setCaretPosition(0);
        moDateStartPlanned.setValue(moRegistry.getDateStartPlanned());
        moDateEndPlanned.setValue(moRegistry.getDateEndPlanned());
        moKeyDeliverable.setValue(new int[] { moRegistry.getFkDeliverableProjectId_n(), moRegistry.getFkDeliverableDeliverableId_n() });

        setFormEditable(true);
        
        moTextCode.setEditable(false);
        
        setFieldsStatusByProject();
        
        if (moRegistry.isRegistryNew()) {
            if (moRegistry.getPkProjectId() == SLibConsts.UNDEFINED) {
                moKeyGroupStagePhase.resetGroup();
            }
            else {
                moKeyProjectStagePhase.setEnabled(moKeyProjectStagePhase.getItemCount() > 0);
            }
        }
        else {
            moKeyProject.setEnabled(false);
            moKeyProjectStage.setEnabled(false);
            moKeyProjectStagePhase.setEnabled(false);
        }
        
        addAllListeners();
    }

    @Override
    public SDbProjectStagePhaseActivity getRegistry() throws Exception {
        SDbProjectStagePhaseActivity registry = moRegistry.clone();

        if (registry.isRegistryNew()) {
            registry.setPkProjectId(moKeyProjectStagePhase.getValue()[0]);
            registry.setPkStageId(moKeyProjectStagePhase.getValue()[1]);
            registry.setPkPhaseId(moKeyProjectStagePhase.getValue()[2]);
        }

        registry.setCode(moTextCode.getValue());
        registry.setName(moTextName.getValue());
        registry.setDescription(SLibUtils.textTrim(jtaDescription.getText()).replaceAll("'", "''"));
        registry.setDateStartPlanned(moDateStartPlanned.getValue());
        registry.setDateEndPlanned(moDateEndPlanned.getValue());
        //registry.setDeleted(...);
        //registry.setSystem(...);
        if (moKeyDeliverable.getSelectedIndex() <= 0) {
            registry.setFkDeliverableProjectId_n(SLibConsts.UNDEFINED);
            registry.setFkDeliverableDeliverableId_n(SLibConsts.UNDEFINED);
        }
        else {
            registry.setFkDeliverableProjectId_n(moKeyDeliverable.getValue()[0]);
            registry.setFkDeliverableDeliverableId_n(moKeyDeliverable.getValue()[1]);
        }

        return registry;
    }

    @Override
    public SGuiValidation validateForm() {
        SGuiValidation validation = moFields.validateFields();
        
        if (validation.isValid()) {
            if (moDateStartPlanned.getValue().after(moDateEndPlanned.getValue())) {
                validation.setMessage(SGuiConsts.ERR_MSG_FIELD_DATE_ + "'" + SGuiUtils.getLabelName(jlDateStartPlanned) + "'" + SGuiConsts.ERR_MSG_FIELD_DATE_LESS_EQUAL + "'" + SGuiUtils.getLabelName(jlDateEndPlanned) + "'.");
                validation.setComponent(moDateStartPlanned);
            }
        }
        
        return validation;
    }

    @Override
    public void itemStateChanged(ItemEvent e) {
        if (e.getSource() instanceof SBeanFieldKey) {
            if (e.getStateChange() == ItemEvent.SELECTED) {
                SBeanFieldKey field = (SBeanFieldKey) e.getSource();
                
                if (field == moKeyProject) {
                    itemStateChangedProject();
                }
            }
        }
    }
